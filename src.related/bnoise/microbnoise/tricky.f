	SUBROUTINE TRICKY(FMT,BL,D)
	CHARACTER*5 FMT(18)
	CHARACTER*1 BL
	DIMENSION D(15)
	DO 10 I=2,15
	J=2
	IF(D(I).GE.1.0.AND.D(I).LT.10.0) J=3
	IF(D(I).GE.0.0.AND.D(I).LT.1.0) J=4
	IF(D(I).LE.-10.0)  J=1
	IF(D(I).LE.-1.0.AND.D(I).GT.-10.0) J=2
	IF(D(I).LT.0.0.AND.D(I).GT.-1.0) J=3
10	WRITE(FMT(I+2),35)J
35	FORMAT(',F5.',I1)
	CALL TRICK2(FMT,D,BL)
	RETURN
	END
	SUBROUTINE TRICK2(F,D,B)
	CHARACTER*90 F
	CHARACTER*1 B
	DIMENSION D(15)
	WRITE(7,F)B,D
	RETURN
	END
	SUBROUTINE TRICK3(NWC,PW)
	DIMENSION PW(11)
	CHARACTER*5 FMT(14)
	DATA FMT(1),FMT(2),FMT(14)/'(2X,I','2   ',')    '/
	DO 10  I=1,11
	J=5
	IF(PW(I).GE.10.0) J=4
	IF(PW(I).GE.100.) J=3
10	WRITE(FMT(I+2),20)  J
20	FORMAT(',F7.',I1)
	CALL TRICK4(FMT,NWC,PW)
	RETURN
	END
	SUBROUTINE TRICK4(F,N,P)
	CHARACTER*70 F
	DIMENSION P(11)
	WRITE(7,F) N,P
	RETURN
	END
