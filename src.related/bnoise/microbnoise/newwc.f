	SUBROUTINE NEWWC(PW,NAME,DD)
	DIMENSION PW(11),DD(15)
	CHARACTER*10  NAME(2)
10	WRITE(0,20)
20	FORMAT(/' PROJECTILE WEIGHT: ')
	READ(5,*,ERR=10)PW(1)
	DO 50 I=1,10
30	WRITE(0,40)I
40	FORMAT(/' PROPELLANT WEIGHT FOR CHARGE ZONE ',I2,': ')
50	READ(5,*,ERR=30)PW(I+1)
	WRITE(0,60)
60	FORMAT(/' NAME OF WEAPON: ')
	READ(5,70)NAME
70	FORMAT(2A10)
80	WRITE(0,90)'A'
90	FORMAT(/' PARAMETER ',A1,': ')
	READ(5,*,ERR=80)DD(1)
100	WRITE(0,90)'B'
	READ(5,*,ERR=100)DD(2)
	DO 130 I=0,5
110	WRITE(0,120)30*I
120	FORMAT(/' DECIBEL DIFFERENCE AT ',I3,' DEGREES: ')
130	READ(5,*,ERR=110)DD(I+3)
	DD(9)=0.0
	DO 140  I=10,14
140	DD(I)=DD(18-I)
150	WRITE(0,160)
160	FORMAT(/' AVERAGE DECIBEL DIFFERENCE: ')
	READ(5,*,ERR=150)DD(15)
	RETURN
	END
