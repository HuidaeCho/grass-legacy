	SUBROUTINE EBOUND(XMIN,YMIN,XMAX,YMAX)
	WRITE(7,10)
10	FORMAT(1H ,'BOUNDS',74X)
15	WRITE(0,20)
20	FORMAT(/' MINIMUM X COORDINATE: ')
	READ(5,*,ERR=15)XMIN
30	WRITE(0,40)
40	FORMAT(/' MINIMUM Y COORDINATE: ')
	READ(5,*,ERR=30)YMIN
50	WRITE(0,60)
60	FORMAT(/' MAXIMUM X COORDINATE: ')
	READ(5,*,ERR=50)XMAX
70	WRITE(0,80)
80	FORMAT(/' MAXIMUM Y COORDINATE: ')
	READ(5,*,ERR=70)YMAX
	WRITE(7,90)XMIN,YMIN
90	FORMAT(1H ,2F10.0,60X)
	WRITE(7,90)XMAX,YMAX
	RETURN
	END
