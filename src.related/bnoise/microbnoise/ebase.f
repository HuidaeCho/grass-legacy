	SUBROUTINE EBASE(XMIN,YMIN,XMAX,YMAX)
	CHARACTER*1 STAR,BL,ANS
	DATA STAR/'*'/,BL/' '/
	WRITE(7,10)
10	FORMAT(1H ,'BASE',76X)
20	WRITE(0,30)
	K=0
30	FORMAT(/' STARTING NEW OUTLINE')
40	WRITE(0,50)
	K=K+1
50	FORMAT(/' X COORDINATE: ')
	READ(5,*,ERR=40)X
	IF(X.GE.XMIN.AND.X.LE.XMAX) GO TO  60
	WRITE(0,55)
55	FORMAT(' COORDINATE NOT WITHIN BOUNDS')
	K=K-1
	GO TO  40
60	WRITE(0,70)
70	FORMAT(/' Y COORDINATE: ')
	READ(5,*,ERR=60)Y
	IF(Y.GE.YMIN.AND.Y.LE.YMAX)  GO TO 75
	WRITE(0,55)
	GO TO 60
75	CONTINUE
	IF(K.LT.2) GO TO 105
80	WRITE(0,90)
90	FORMAT(/' IS THIS THE END OF A CONTINUOUS LINE? ')
	READ(5,100)ANS
100	FORMAT(A1)
	IF(ANS.EQ.'Y') GO TO 120
	IF(ANS.NE.'N') GO  TO 80
105	CONTINUE
	WRITE(7,110)X,Y,BL
110	FORMAT(1H ,2F10.0,A1,59X)
	GO TO 40
120	WRITE(7,110)X,Y,STAR
125	WRITE(0,130)
130	FORMAT(/' IS THIS THE LAST LINE? ')
	READ(5,100)ANS
	IF(ANS.EQ.'N') GO TO 20
	IF(ANS.NE.'Y') GO TO  125
	WRITE(7,140)STAR
140	FORMAT(1H ,20X,A1,59X)
	RETURN
	END
