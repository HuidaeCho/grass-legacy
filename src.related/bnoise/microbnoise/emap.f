	SUBROUTINE EMAP(DAYS)
	INTEGER*2 I(6)
	CHARACTER*1  ANS
	WRITE(7,10)
10	FORMAT(1H ,'MAP',77X)
	DO 15 J=1,6
15	I(J)=1
	WRITE(0,20)
20	FORMAT(/' PRINT DATA BASE INFORMATION? ')
	READ(5,30)ANS
30	FORMAT(A1)
	IF(ANS.EQ.'N') I(1)=0
	WRITE(0,40)
40	FORMAT(/' PRINT TARGET VS FIRING POINT TABLE? ')
	READ(5,30)ANS
	IF(ANS.EQ.'N') I(2)=0
	WRITE(0,50)
50	FORMAT(/' PRINT TARGET VS GUN TYPE TABLE? ')
	READ(5,30)ANS
	IF(ANS.EQ.'N') I(3)=0
	WRITE(0,60)
60	FORMAT(/' PRINT GUN TYPE VS TARGET TABLE? ')
	READ(5,30)ANS
	IF(ANS.EQ.'N') I(4)=0
	WRITE(0,70)
70	FORMAT(/' PRINT GUN TYPE VS FIRING POINT TABLE? ')
	READ(5,30)ANS
	IF(ANS.EQ.'N') I(5)=0
	WRITE(0,80)
80	FORMAT(/' PRINT "EXTRANEOUS DATA" MESSAGE? ')
	READ(5,30)ANS
	IF(ANS.EQ.'Y') I(6)=0
	WRITE(7,90)I
90	FORMAT(1H ,6I1,74X)
95	WRITE(0,100)
100	FORMAT(/' NUMBER OF DAYS OF INFORMATION IN DATA BASE [1]: ')
	READ(5,*,ERR=95)DAYS
	WRITE(7,110)DAYS
110	FORMAT(1H ,F10.0,70X)
120	WRITE(0,130)
130	FORMAT(/' NUMBER OF GRID SIZES TO BE TESTED: ')
	READ(5,*,ERR=120)NG
	IF(NG.LE.0) GO TO 120
	WRITE(7,140)NG
140	FORMAT(1H ,I2,78X)
	DO 200 J=1,NG
150	WRITE(0,160)
160	FORMAT(/' GRID SIZE: ')
	READ(5,*,ERR=150)GS
200	WRITE(7,110)GS
	RETURN
	END
