	SUBROUTINE ESCAT(DAYS)
	CHARACTER*1 BL,ANS,IK(4)
	CHARACTER*2 IDGUN(28),BL2
	DATA BL,BL2/' ','  '/
	WRITE(7,10)
10	FORMAT(1H ,'SCATTER',73X)
	DO20 I=1,4
20	IK(I)=BL
	DO  30 I=1,28
30	IDGUN(I)=BL2
	N=1
40	WRITE(0,50)
50	FORMAT(/' SCATTER INFORMATION COLLECTED FOR TARGETS? ')
	READ(5,60)ANS
60	FORMAT(A1)
	IF(ANS.EQ.'N') GO TO 70
	IF(ANS.NE.'Y') GO TO 40
	IK(N)='T'
	N=N+1
70	WRITE(0,80)
80	FORMAT(/' SCATTER INFORMATION COLLECTED FOR FIRING POINTS? ')
	READ(5,60)ANS
	IF(ANS.EQ.'N') GO TO 90
	IF(ANS.NE.'Y') GO  TO 70
	IK(N)='F'
	N=N+1
90	WRITE(0,100)
100	FORMAT(/' DAY (D), NIGHT (N) OR BOTH (B) DATA: ')
	READ(5,60)IK(N)
	N=N+1
110	WRITE(0,120)
120	FORMAT(/' DO YOU WANT TO SPECIFY GUN TYPES? ')
	READ(5,60)ANS
	IF(ANS.EQ.'N')  GO TO 170
	IF(ANS.NE.'Y') GO TO 110
	IK(N)='G'
	DO 160 I=1,28
	WRITE(0,130)
130	FORMAT(/' GUN TYPE: ')
	READ(5,140)IDGUN(I)
140	FORMAT(A2)
	IF(I.EQ.28) GO  TO 170
145	WRITE(0,150)
150	FORMAT(/' MORE GUN TYPES? ')
	READ(5,60)ANS
	IF(ANS.EQ.'N') GO TO 170
	IF(ANS.NE.'Y') GO TO 145
160	CONTINUE
170	WRITE(0,180)
180	FORMAT(/' MULTIPLIER [1]: ')
	READ(5,*,ERR=170)FACT
190	WRITE(0,200)
200	FORMAT(/' STANDARD DEVIATION [300]: ')
	READ(5,*,ERR=190)SD
	WRITE(7,210)IK,IDGUN,FACT,SD
210	FORMAT(1H ,4A1,28A2,F10.2,F10.0)
	WRITE(7,220)DAYS
220	FORMAT(1H ,F10.0,70X)
	RETURN
	END
