	SUBROUTINE EFORM(DAYS)
	CHARACTER*4 AVE
	WRITE(7,10)
10	FORMAT(1H ,'FORM-A',74X)
	WRITE(0,20)
20	FORMAT(/' CHARGE AVERAGING TECHNIQUE (MAX,IAVE,CAVE): ')
	READ(5,30)AVE
30	FORMAT(A4)
40	WRITE(0,50)
50	FORMAT(/' GROUND CORRECTION (dB) [1.5]: ')
	READ(5,*,ERR=40)GC
	WRITE(7,60)AVE,GC
60	FORMAT(1H ,A4,6X,F10.2,60X)
	WRITE(7,70)DAYS
70	FORMAT(1H ,F10.0,70X)
	RETURN
	END
