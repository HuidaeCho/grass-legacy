	SUBROUTINE EPOINT
	CHARACTER*1 ANS,STAR,BL
	CHARACTER*9 FLAG
	DIMENSION R(3)
	CHARACTER*5 IB
	DATA STAR,BL/'*',' '/
	WRITE(7,10)
10	FORMAT(1H ,'POINT',75X)
	DO 40 I=1,3
20	WRITE(0,30)I
30	FORMAT(/' INVERSION FACTOR ',I1,': ')
	READ(5,*,ERR=20)R(I)
40	CONTINUE
	WRITE(0,60)
60	FORMAT(/' "DAY", "NIGHT" OR "BOTH": ')
	READ(5,70)IB
70	FORMAT(A5)
	WRITE(7,80)R,IB
80	FORMAT(1H ,3F10.1,A5,45X)
90	WRITE(0,100)
100	FORMAT(/' NAME OF X,Y LOCATION: ')
	READ(5,110)FLAG
110	FORMAT(A9)
120	WRITE(0,130)
130	FORMAT(/' X COORDINATE: ')
	READ(5,*,ERR=120)X
140	WRITE(0,150)
150	FORMAT(/' Y COORDINATE: ')
	READ(5,*,ERR=140)Y
160	WRITE(0,170)
170	FORMAT(/' ANY MORE POINTS? ')
	READ(5,180)ANS
180	FORMAT(A1)
	IF(ANS.EQ.'N') GO TO  200
	IF(ANS.NE.'Y') GO TO 160
	WRITE(7,190)BL,FLAG,X,Y
190	FORMAT(1H ,A1,A9,2F10.0,50X)
	GO TO 90
200	WRITE(7,190)STAR,FLAG,X,Y
	RETURN
	END
