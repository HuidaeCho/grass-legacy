	SUBROUTINE ELOC
	CHARACTER*10 CHOICE,NAME,LOCATE
	CHARACTER*1  ANS
	WRITE(7,10)
10	FORMAT(1H ,'LOCATOR',73X)
	NAME='          '
	LOCATE='         '
	WRITE(0,20)
20	FORMAT(/' "ALL", "TARGET" OR "FIRING": ')
	READ(5,30)CHOICE
30	FORMAT(A10)
	WRITE(0,40)
40	FORMAT(/' PRINT ID ON  PLOT? ')
	READ(5,50)ANS
50	FORMAT(A1)
	IF(ANS.EQ.'Y') NAME='NAME'
	WRITE(0,60)
60	FORMAT(/' PRINT COORDINATES? ')
	READ(5,50)ANS
	IF(ANS.EQ.'Y') LOCATE='LOCATION'
70	WRITE(0,80)
80	FORMAT(/' SIZE OF LETTERS [0.14]: ')
	READ(5,*,ERR=70)S
90	WRITE(0,100)
100	FORMAT(/' ROTATION OF LETTERS [0 DEG]: ')
	READ(5,*,ERR=90)R
	WRITE(7,110)CHOICE,NAME,LOCATE,S,R
110	FORMAT(1H ,3A10,2F10.3,30X)
	RETURN
	END
